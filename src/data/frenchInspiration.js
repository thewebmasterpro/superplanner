/** Pick a random item from an array, different from the previous one if possible */
export function pickRandom(array, previousText = null) {
    if (array.length <= 1) return array[0]
    let item
    do {
        item = array[Math.floor(Math.random() * array.length)]
    } while (item.text === previousText || item === previousText)
    return item
}
